package ch03;

public class CompareEx {
	public static void main(String[] args) {
		int a = 65;
		char c = 'A';
		System.out.println(a==c); //비교연산_Equal(a와 c는 같냐)
		System.out.println(a = c); //대입연산(c의 값을 a에 대입)
		System.out.println('A' != 65); //비교연산('A'와 65는 다르냐) not Equal
		
		
		
		
		
	}
}
