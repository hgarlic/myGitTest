package ch03;

public class CalEx2 {
	public static void main(String[] args) {
		int a = 20;
		int b = 8;
		System.out.printf("a+b=%d\n", a+b);
		//앞의 "a+b=%d는 계산이 아니라 앞에 계산식 써주고, 형식 정하는 부분임
		System.out.printf("a-b=%d\n", a-b);
		System.out.printf("a*b=%d\n", a*b);
		System.out.printf("a/b=%d\n", a/b);
		System.out.printf("a%%b=%d\n", a%b);
		// 역슬래쉬(\)와 퍼센트(%)는 두개씩 써야 계산으로 안되고, 문자처럼 나옴
		
		
		
	}
}
